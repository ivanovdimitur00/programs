#include <iostream>
using namespace std;

int main()
{
	int quarter;
	cin>>quarter;
	
	if (quarter == 0){
	cout<<"Spring"<<endl;
	} 
	if (quarter == 1){
		cout<<"Summer"<<endl;
	}
	if (quarter == 2){
		cout<<"Autumn"<<endl;
	}
	if (quarter == 3){
		cout<<"Winter"<<endl;
	}
	
	system("pause");
	return 0;
}
